    private void getProducto(String sql){
        try {
            int f=0;
            ResultSet rs=cn.Consulta(sql);
            tbl.setRowCount(0);
            while(rs.next()){
                f=tbl.getRowCount();
                tbl.setRowCount(f+1);
                tbl.setValueAt(rs.getInt("id_producto"), f, 0);
                tbl.setValueAt(rs.getString("nombre_producto"), f, 1);
                tbl.setValueAt(rs.getString("nombres"), f, 2);//tipo 
                tbl.setValueAt(rs.getString("precio"), f, 3);
                tbl.setValueAt(rs.getDate("caducidad"), f, 4);
                tbl.setValueAt(rs.getInt("existencia"), f, 5);
                tbl.setValueAt(rs.getString("descripcion"), f, 6);
                tbl.setValueAt(rs.getString("especificaciones"), f, 7);
            }
            rs.close();
        } catch (SQLException ex) {
            System.out.println("Error al consultar cliente");
        }
    }
