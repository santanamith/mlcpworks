    private void getRuc(){
        try {
            ResultSet rs=cn.Consulta("select * from ruc_pharmacy");
            if(rs.next()){
                txtID.setText(rs.getInt("id_ruc")+"");
                txtRuc.setText(rs.getString("ruc"));
            }
            rs.close();
        } catch (SQLException ex) {
            System.out.println("Error al obtener Ruc");
        }
    }
