    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if(!txtRuc.getText().trim().isEmpty()|| txtRuc.getText()!=null){
                cn.ejecutar("Update RUC_PHARMACY set ruc='"+txtRuc.getText()+"' where id_RUC="+txtID.getText());
                JOptionPane.showMessageDialog(null, "Ruc : actualizado.");
                getRuc();
                btnEditar.setVisible(true);
                btnGuardar.setVisible(false);
                txtRuc.setEditable(false);
            }else{
                JOptionPane.showMessageDialog(null, "Complete el campo.");
            }
        } catch (SQLException ex) {
            System.out.println("Erro al actualizar "+ex.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

